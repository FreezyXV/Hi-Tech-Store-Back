// MongoDB connection
async function connectDB() {
  try {
    await mongoose.connect(mongoURI);
    console.log("Connected to MongoDB!");
  } catch (error) {
    console.error("Error connecting to MongoDB:", error.message);
    process.exit(1);
  }
}

// Create or update a category
async function createCategory(categoryData) {
  let category = await Category.findOne({ name: categoryData.name });
  if (!category) {
    category = new Category({
      name: categoryData.name,
      imageUrls: categoryData.imageUrls,
      description: categoryData.description,
      brands: [],
    });
    await category.save();
  }
  return category;
}

// Create or update a brand
async function createBrand(brandData, category) {
  let brand = await Brand.findOne({
    name: brandData.name,
    categoryId: category._id,
  });
  if (!brand) {
    brand = new Brand({
      name: brandData.name,
      categoryId: category._id,
      models: [],
    });
    await brand.save();
  }

  if (!category.brands.some((id) => id.toString() === brand._id.toString())) {
    category.brands.push(brand._id);
    await category.save();
  }

  return brand;
}

// Create or update a model
async function createModel(modelData, brand, category) {
  let model = await Model.findOne({
    name: modelData.name,
    brandId: brand._id,
  });
  if (!model) {
    model = new Model({
      name: modelData.name,
      imageUrls: modelData.imageUrls || [],
      features: modelData.features || [],
      specifications: modelData.specifications || {},
      screen: modelData.screen || {},
      battery: modelData.battery || {},
      connectivity: modelData.connectivity || {},
      releaseDate: modelData.releaseDate || new Date(),
      brandId: brand._id,
      brandName: brand.name,
      categoryId: category._id,
      categoryName: category.name,
      variants: [],
    });
    await model.save();
  }

  if (!brand.models.some((id) => id.toString() === model._id.toString())) {
    brand.models.push(model._id);
    await brand.save();
  }

  return model;
}

// Create or update a variant
async function createVariant(variantData, model) {
  const sku =
    variantData.sku ||
    `${model.name}-${variantData.color || ""}-${variantData.storage || ""}`
      .replace(/\s+/g, "-")
      .toUpperCase();

  if (
    !variantData.price ||
    !variantData.imageUrls ||
    !variantData.imageUrls.length
  ) {
    throw new Error(
      `Missing required fields for variant: ${JSON.stringify(variantData)}`
    );
  }

  variantData.imageUrls = Array.isArray(variantData.imageUrls)
    ? variantData.imageUrls
    : [variantData.imageUrls];

  let variant = await Variant.findOne({ sku, modelId: model._id });
  if (!variant) {
    variant = new Variant({
      name: model.name,
      imageUrls: variantData.imageUrls,
      price: variantData.price,
      stock: variantData.stock || 10,
      color: variantData.color,
      size: variantData.size || null,
      ram: variantData.ram || null,
      chip: variantData.chip || null,
      storage: variantData.storage || null,
      modem: variantData.modem || null,
      bracelet: variantData.bracelet || null,
      braceletColor: variantData.braceletColor || null,
      sku,
      modelId: model._id,
    });
    await variant.save();
  }

  if (!model.variants.some((id) => id.toString() === variant._id.toString())) {
    model.variants.push(variant._id);
    await model.save();
  }

  return variant;
}

// Seed categories, brands, models, and variants
async function seedDatabase() {
  try {
    await connectDB();

    for (const categoryData of seedData) {
      console.log(`Seeding category: ${categoryData.name}`);
      const category = await createCategory(categoryData);

      for (const brandData of categoryData.brands) {
        console.log(`  Seeding brand: ${brandData.name}`);
        const brand = await createBrand(brandData, category);

        for (const modelData of brandData.models) {
          console.log(`    Seeding model: ${modelData.name}`);
          const model = await createModel(modelData, brand, category);

          for (const variantData of modelData.variants) {
            try {
              console.log(
                `      Seeding variant: ${variantData.color || ""}, ${
                  variantData.storage || ""
                }`
              );
              await createVariant(variantData, model);
            } catch (error) {
              console.error(
                `Error seeding variant (${variantData.color || ""}, ${
                  variantData.storage || ""
                }):`,
                error.message
              );
            }
          }
        }
      }
    }

    console.log("Database seeded successfully!");
  } catch (error) {
    console.error("Error seeding database:", error.message);
  } finally {
    await mongoose.connection.close();
    console.log("Database connection closed.");
  }
}

// Run the seeding script
seedDatabase();
